//等差数列
/*****************************************
 *功能:等差数列 2，5，8，11，14。。。。
 *输入:正整数N >0
 *输出:求等差数列前N项和
 *返回:转换成功返回 0 ,非法输入与异常返回-1
******************************************/
#include <stdio.h>

int main(void)
{
    int n, nums, i;
    nums = i = 0;
    scanf("%d",&n);
    for(i = 0; i < n; i++)
    {
        nums += (2 + 3 * i);
    }
    printf("%d",nums);

    return 0;
}